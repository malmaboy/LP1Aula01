using System;

namespace Argumentos {
    class Program {
        /// <summary>
        ///  it doe a for.
        /// </summary>
        /// <param name="args">
        /// you cant do comments here for the args argument. 
        /// </param>
        static void Main (string[] args) {
            for (int i = 0; i < args.Length; i++) {
                Console.WriteLine (args[i]);
            }
        }
    }
}

